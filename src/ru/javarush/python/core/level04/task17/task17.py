"""
Напишите функцию calculate_total_cost(price, tax=0.2), которая принимает цену товара
 и необязательный параметр налог (по умолчанию 20%). Функция должна вычислять и выводить
 общую стоимость товара с учетом налога. Затем напишите программу, которая вызывает
 эту функцию с различными параметрами.
Требования:
•	Программа должна включать функцию calculate_total_cost, которая принимает два параметра:
цену товара и необязательный параметр налог (по умолчанию 20%).
•	Функция calculate_total_cost должна вычислять общую стоимость товара с учетом налога.
•	Функция calculate_total_cost должна выводить общую стоимость товара с учетом налога.
•	Программа должна вызывать функцию calculate_total_cost с различными параметрами.
"""
def calculate_total_cost(price, tax=0.2):
    total_cost = price * (1 + tax)
    print(f"Цена товара: {price:.2f}, Налог: {tax * 100:.0f}%, Общая стоимость: {total_cost:.2f}")

calculate_total_cost(100)  # Используется налог по умолчанию (20%)
calculate_total_cost(250.75, 0.15)  # Налог 15%
calculate_total_cost(89.99, 0.1)  # Налог 10%
calculate_total_cost(500, 0.25)  # Налог 25%
calculate_total_cost(0)  # Цена 0, налог по умолчанию
