"""
Напишите программу, которая запрашивает у пользователя два числа. Если пользователь
не вводит значение (пустая строка), используйте значение по умолчанию None для этого числа.
Вычислите и выведите сумму этих чисел.
Требования:
•	Запросить у пользователя ввода двух чисел
•	Использование значения по умолчанию None в случае пустого ввода
•	Если хотя бы одно из чисел равно None, то вывести надпись "Сумма чисел неизвестна".
•	Вычислить сумму двух введенных чисел
•	Вывести сумму чисел на экран

"""
def get_number(prompt):
    user_input = input(prompt)
    if user_input.strip() == "":
        return None
    try:
        return float(user_input)
    except ValueError:
        print("Ошибка: введено не число.")
        return None

num1 = get_number("Введите первое число (или оставьте пустым): ")
num2 = get_number("Введите второе число (или оставьте пустым): ")

if num1 is None or num2 is None:
    print("Сумма чисел неизвестна")
else:
    total = num1 + num2
    print(f"Сумма чисел: {total}")


