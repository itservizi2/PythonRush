"""
Напишите программу, которая запрашивает у пользователя числа и суммирует их,
пока пользователь не введет отрицательное число. Используйте цикл while и
оператор break для завершения ввода при отрицательном числе.
Требования:
•	Программа должна проверять, является ли введенное число отрицательным.
•	Если введенное число отрицательное, программа должна завершить цикл с помощью оператора break.
•	Если введенное число неотрицательное, программа должна добавлять его к переменной для подсчета
суммы (например, total_sum).
•	После завершения ввода программа должна вывести сумму всех введенных положительных чисел.
"""

total_sum = 0
while True:
    try:
        number = int(input("Введите положительное число (отрицательное — для завершения): "))
        if number < 0:
            print("Ввод завершён.")
            break
        total_sum += number
    except ValueError:
        print("Пожалуйста, введите корректное число.")

print("Сумма всех введённых положительных чисел:", total_sum)
